# For loops examples

print('My name is')
for i in range(5):
    print('Jimmy Five Time ' + str(i))

for i in range(12, 16):
    print('Jimmy Five Time ' + str(i))  

for i in range(0, 10):
    print('Jimmy Five Time ' + str(i)) 

for i in range(5, -1, -1):
    print('Jimmy Five Time ' + str(i)) 

for i in range(2, 5, 2):
    print('Jimmy Five Time ' + str(i)) 

